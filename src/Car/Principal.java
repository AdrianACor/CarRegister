    public void buscar(String tipoBusqueda, String valorBusqueda,String filter){
        List<Integer> idCliente = new ArrayList<Integer>();
        if(tipoBusqueda.equals("Propietario")){
            if(!filter.equals("IdCliente")){
                prop.buscarPropietario(filter,valorBusqueda);
            }else{
                idCliente.add(Integer.parseInt(valorBusqueda));
                prop.buscarPropietarioID(idCliente);
            }
            prop.setSize(670, 214);
            prop.setLocation(0,0);
            jPTablas.removeAll();
            jPTablas.add(prop);
            jPTablas.revalidate();
            jPTablas.repaint();
            
            jPTablas2.removeAll();
            jPTablas2.revalidate();
            jPTablas2.repaint();
        }else if(tipoBusqueda.equals("Auto")){

            Autos autos = new Autos(this);
            idCliente = autos.buscarAuto(filter,valorBusqueda);
            autos.setSize(670, 214);
            autos.setLocation(0, 0);
            jPTablas2.removeAll();
            jPTablas2.add(autos);
            jPTablas2.revalidate();
            jPTablas2.repaint();
        
            prop.buscarPropietarioID(idCliente);
            prop.setSize(670, 214);
            prop.setLocation(0,0);
            jPTablas.removeAll();
            jPTablas.add(prop);
            jPTablas.revalidate();
            jPTablas.repaint();
        }
    }
       
