    private void btnBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarMouseClicked
    String TextValue = "";
    String filter = "";
    if(!rbPropietario.isSelected() && !rbAuto.isSelected()){
        JOptionPane.showMessageDialog(this, "Debe realizar una selección");
    }else{
        if (rbPropietario.isSelected() && !rbNombre.isSelected() && !rbApPaterno.isSelected() &&
            !rbApMaterno.isSelected() && !rbIdClienteProp.isSelected() && !rbTelefono.isSelected()) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una opcion de busqueda");
        }else if(rbAuto.isSelected() && !rbPlacas.isSelected() && !rbIdClienteAuto.isSelected())
        {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una opcion de busqueda");
        }

    }
    for (Enumeration<AbstractButton> buttons = bgBusquedas.getElements(); buttons.hasMoreElements();) {
            AbstractButton button = buttons.nextElement();
            if (button.isSelected()) {
                switch (button.getText()){
                    case "Nombre":
                        TextValue = tNombre.getText();
                        filter = "Nombre";
                    break;
                    case "Apellido Paterno":
                        TextValue = tApPaterno.getText();
                        filter = "Apellido_Paterno";
                    break;
                    case "Apellido Materno":
                        TextValue = tApMaterno.getText();
                        filter = "Apellido_Materno";
                    break;
                    case "Telefono":
                        TextValue = tTelefono.getText();
                        filter = "Telefono";
                    break;
                    case "ID cliente":
                        filter = "IdCliente";
                        TextValue = tIdClienteProp.getText();
                    if(tIdClienteProp.getText().isEmpty()){
                        TextValue = tIdClienteAuto.getText();
                    }
                    break;
                    case "Placas":
                        filter = "Placas";
                        TextValue = tPlacas.getText();
                    break;
                }
                if (!TextValue.isEmpty()) {
                    for (Enumeration<AbstractButton> busquedas = bgBuscarPor. getElements(); busquedas.hasMoreElements();) {
                            AbstractButton busquedaPor = busquedas.nextElement();
                            if (busquedaPor.isSelected()) {
                                buscar(busquedaPor.getText(),TextValue,filter);
                            }
                        }
                    /* JOptionPane.showMessageDialog(this, TextValue); */
                } else {
                    JOptionPane.showMessageDialog(this, "Ingresa un valor para el campo " + button.getText());
                }
            }
            else{
                switch (button.getText()){
                    case "Nombre":
                        tNombre.setText("");
                    break;
                    case "Apellido Paterno":
                        tApPaterno.setText("");
                    break;
                    case "Apellido Materno":
                        tApMaterno.setText("");
                    break;
                    case "Telefono":
                        tTelefono.setText("");
                    break;
                    case "ID":
                        tIdClienteProp.setText("");
                    break;
                    case "Placas":
                        tPlacas.setText("");
                    break;
                }
            }
        }
    }//GEN-LAST:event_btnBuscarMouseClicked

