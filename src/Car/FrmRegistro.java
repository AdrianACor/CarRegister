    private void jBGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBGuardarMouseClicked
            String usuario = jTFUsuario.getText();
            String correo = jTCorreo.getText();
            String contrasenia = jTPassword.getText();
            String confContra = jTConfPassword.getText();
        
            if(!usuario.isEmpty()&& !correo.isEmpty() && !contrasenia.isEmpty()){
                
                if(!buscaCorreo(correo)){    
                    if(contrasenia.equals(confContra)){
                        String passwd = Base64.getEncoder().encodeToString(contrasenia.getBytes());
                        insertUsuario(usuario,correo,passwd);
                        
                    }else{
                        JOptionPane.showMessageDialog(this, "Las contraseñas no coinciden");
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "El correo ya se encuentra registrado");
                }
            }else{
                JOptionPane.showMessageDialog(this, "Se deben llenar todos los campos");
            }
        
        
    }//GEN-LAST:event_jBGuardarMouseClicked

