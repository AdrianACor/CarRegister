    public Boolean buscaCorreo(String correo){
     Boolean correoExistente = false;
     Statement st;
     ResultSet rs;
     
     String query = "select * from usuarios where correo = '"+correo+"'";
     
        try{

            st = conectInsertUsuario.createStatement();
            rs = st.executeQuery(query);
            
           while(rs.next()){
                correo = rs.getString(3);
               if(!correo.isEmpty()){
                    correoExistente = true;   
                }
            }
            
        }catch(Exception e){
            System.out.println("Ocurrio un error: "+e);
        }
             
     return correoExistente;   
    }
    
    public void insertUsuario(String usuario, String correo, String contraseña){
        
        String query = "INSERT INTO usuarios(usuario, correo, password)" +
            "VALUES('"+usuario+"','"+correo+"','"+contraseña+"');";

        try {
           
            PreparedStatement execQuery = conectInsertUsuario.prepareStatement(query);
            execQuery.executeUpdate();
            
            int resp = JOptionPane.showConfirmDialog(this,"Usuario agregado correctamente",null,JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE);
            System.out.println(resp);
            this.dispose();
               
            } catch(Exception e){
            System.out.println("Error al agregar propietario: "+e);
        } 
    }

